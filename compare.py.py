def get_atom_features(atom):
    atom_type = [0] * len(Config.ATOM_LIST)
    symbol = atom.GetSymbol()
    if symbol in Config.ATOM_LIST:
        atom_type[Config.ATOM_LIST.index(symbol)] = 1
    else:
        atom_type[Config.ATOM_LIST.index('*')] = 1

    hybridization = [0, 0, 0, 0, 0]
    hyb = atom.GetHybridization()
    hyb_types = [Chem.rdchem.HybridizationType.SP, Chem.rdchem.HybridizationType.SP2,
                 Chem.rdchem.HybridizationType.SP3, Chem.rdchem.HybridizationType.SP3D,
                 Chem.rdchem.HybridizationType.SP3D2]
    if hyb in hyb_types:
        hybridization[hyb_types.index(hyb)] = 1

    properties = [
        atom.GetFormalCharge(), atom.GetDegree(), int(atom.GetIsAromatic()),
        int(atom.IsInRing()), atom.GetTotalNumHs(), atom.GetImplicitValence(),
        atom.GetMass() / 100.0, int(atom.GetChiralTag() != Chem.rdchem.ChiralType.CHI_UNSPECIFIED)
    ]
    return atom_type + hybridization + properties

def get_bond_features(bond):
    bond_type = [0, 0, 0, 0]
    bt = bond.GetBondType()
    if bt in Config.BOND_TYPES:
        bond_type[Config.BOND_TYPES[bt]] = 1
    features = [
        int(bond.GetIsConjugated()), int(bond.IsInRing()),
        int(bond.GetStereo() != Chem.rdchem.BondStereo.STEREONONE),
        bond.GetBondTypeAsDouble() / 3.0, int(bond.GetIsAromatic()), 1.0
    ]
    return bond_type + features

def smiles_to_enhanced_graph(smiles):
    mol = Chem.MolFromSmiles(smiles)
    if mol is None: return None, None, None
    node_features = [get_atom_features(atom) for atom in mol.GetAtoms()]
    edge_index, edge_features = [], []
    for bond in mol.GetBonds():
        i, j = bond.GetBeginAtomIdx(), bond.GetEndAtomIdx()
        bond_feat = get_bond_features(bond)
        edge_index.extend([[i, j], [j, i]])
        edge_features.extend([bond_feat, bond_feat])
    if not edge_index:
        edge_index, edge_features = [[0, 0]], [[0] * Config.EDGE_FEATURES]
    return torch.tensor(node_features, dtype=torch.float), \
           torch.tensor(edge_index, dtype=torch.long).t().contiguous(), \
           torch.tensor(edge_features, dtype=torch.float)

def smiles_to_label_encoding(smiles, max_len=Config.MAX_SMILES_LEN):
    toks = [Config.SMILES_DICT.get(char, 0) for char in smiles]
    return toks[:max_len] if len(toks) > max_len else toks + [0] * (max_len - len(toks))

def smiles_to_onehot_encoding(smiles, max_len=Config.MAX_SMILES_LEN):
    one_hot = np.zeros((max_len, Config.NUM_CHARS), dtype=np.float32)
    for i, char in enumerate(smiles[:max_len]):
        if char in Config.SMILES_DICT: one_hot[i, Config.SMILES_DICT[char] - 1] = 1
    return one_hot

def generate_fingerprints(smiles_list, radius=2, n_bits=2048):
    fps = []
    for smiles in smiles_list:
        mol = Chem.MolFromSmiles(smiles)
        if mol is not None:
            fp = AllChem.GetMorganFingerprintAsBitVect(mol, radius, nBits=n_bits)
            fps.append(np.array(fp))
        else:
            fps.append(np.zeros((n_bits,)))
    return np.array(fps)

