# 2. GENERATE SYNTHETIC PCA DATA (For Visualization)
# ============================================================
def generate_mock_pca_data(num_samples=400):
    """Generates synthetic 2D points to mimic PCA output for the plots."""
    np.random.seed(42)

    # Generate 2 clusters for Inactive (0) and Active (1)
    X_inactive = np.random.normal(loc=[-1, -1], scale=1.5, size=(num_samples // 2, 2))
    X_active = np.random.normal(loc=[1, 1], scale=1.5, size=(num_samples // 2, 2))

    X_pca = np.vstack((X_inactive, X_active))
    y_true = np.array([0] * (num_samples // 2) + [1] * (num_samples // 2))

    # Simulate model predictions based roughly on their accuracies
    preds_dict = {}
    for i, model in enumerate(models):
        error_rate = 1.0 - accs[i]
        flips = np.random.rand(num_samples) < error_rate
        # Flip labels where errors occur to simulate misclassifications
        preds = np.where(flips, 1 - y_true, y_true)
        preds_dict[model] = preds

    return X_pca, y_true, preds_dict

